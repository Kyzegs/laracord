<?php

declare(strict_types=1);

/*
 * Generate typed resource clients and the @method docblocks that give IDEs and
 * PHPStan full autocompletion for the otherwise magic `__call` based API:
 *
 *   $client->channels()->get(['channel_id' => '123']);
 *           ^ resource   ^ endpoint
 *
 * Every resource in the EndpointCatalog gets a thin generated subclass of
 * ResourceClient under src/Resources/Generated whose class docblock lists each
 * endpoint as an @method. The DiscordClient docblock is rewritten to expose each
 * resource accessor as an @method returning the matching generated class.
 *
 * Update the generated files:
 *   $ composer ide-helpers
 *   $ php -f ./bin/resources.php
 *
 * Verify they are up to date (CI):
 *   $ php -f ./bin/resources.php -- --lint
 */

use Illuminate\Support\Str;
use Kyzegs\Laracord\Endpoints\EndpointCatalog;
use Kyzegs\Laracord\LaracordManager;

require __DIR__.'/../vendor/autoload.php';

$linting = in_array('--lint', $argv, true);
$root = dirname(__DIR__);
$generatedDir = $root.'/src/Resources/Generated';

// Mirror the ResourceClient::call() signature minus the leading $endpoint argument.
$signature = implode(', ', [
    'array<string, string|int|\Stringable> $parameters = []',
    'array<string, mixed>|\Illuminate\Contracts\Support\Arrayable<string, mixed>|\JsonSerializable|null $body = null',
    'array<string, mixed> $query = []',
    'list<array<string, mixed>> $files = []',
    '\Kyzegs\Laracord\ValueObjects\AuditLogReason|null $auditLogReason = null',
]);

/** @var array<string, string> $expected map of absolute path => file contents */
$expected = [];

$resourceAccessors = [];

foreach (EndpointCatalog::all() as $resource => $endpoints) {
    $class = Str::studly($resource).'Resource';
    $resourceAccessors[$resource] = $class;

    $methods = [];
    foreach (array_keys($endpoints) as $endpoint) {
        $methods[] = " * @method \Kyzegs\Laracord\Http\DiscordResponse {$endpoint}({$signature})";
    }

    $methodBlock = implode(PHP_EOL, $methods);

    $expected[$generatedDir.'/'.$class.'.php'] = <<<PHP
        <?php

        declare(strict_types=1);

        namespace Kyzegs\Laracord\Resources\Generated;

        use Kyzegs\Laracord\Resources\ResourceClient;

        /**
        {$methodBlock}
         *
         * @internal Generated by bin/resources.php. Do not edit by hand.
         */
        final readonly class {$class} extends ResourceClient {}

        PHP;
}

// Build the DiscordClient resource-accessor docblock.
ksort($resourceAccessors);
$accessorLines = [];
foreach ($resourceAccessors as $resource => $class) {
    $accessorLines[] = " * @method \Kyzegs\Laracord\Resources\Generated\\{$class} {$resource}()";
}

$discordClientDocblock = "/**\n * Resource accessors (generated by bin/resources.php).\n *\n".implode(PHP_EOL, $accessorLines)."\n */\n";

$discordClientPath = $root.'/src/DiscordClient.php';
$discordClientSource = file_get_contents($discordClientPath);

// Replace an existing generated docblock or insert a fresh one above the class.
$marker = '/\/\*\*\n \* Resource accessors \(generated by bin\/resources\.php\)\.\n.*?\*\/\n/s';
if (preg_match($marker, $discordClientSource) === 1) {
    $newDiscordClientSource = preg_replace($marker, $discordClientDocblock, $discordClientSource);
} else {
    $newDiscordClientSource = (string) preg_replace(
        '/(final readonly class DiscordClient)/',
        $discordClientDocblock.'$1',
        $discordClientSource,
        1
    );
}

$expected[$discordClientPath] = $newDiscordClientSource;

// Build the Laracord facade docblock from the LaracordManager proxy. The facade
// resolves to LaracordManager (see ServiceProvider alias) and proxies its public
// methods statically via __callStatic, so they need @method static annotations.
$facadeProxy = LaracordManager::class;
$facadePath = $root.'/src/Facades/Laracord.php';

$facadeMethods = [];
foreach ((new ReflectionClass($facadeProxy))->getMethods(ReflectionMethod::IS_PUBLIC) as $method) {
    if ($method->isStatic() || $method->isConstructor() || str_starts_with($method->getName(), '__')) {
        continue;
    }

    $parameters = [];
    foreach ($method->getParameters() as $parameter) {
        $type = resolveReflectionType($parameter->getType());
        $rest = $parameter->isVariadic() ? '...' : '';
        $default = $parameter->isOptional() && ! $parameter->isVariadic()
            ? ' = '.resolveDefaultValue($parameter)
            : '';
        $parameters[] = trim($type.' '.$rest.'$'.$parameter->getName()).$default;
    }

    $return = resolveReflectionType($method->getReturnType());
    $facadeMethods[] = " * @method static {$return} {$method->getName()}(".implode(', ', $parameters).')';
}

$facadeDocblock = "/**\n".implode(PHP_EOL, $facadeMethods)."\n *\n * @see LaracordManager\n */";

$facadeSource = file_get_contents($facadePath);
$expected[$facadePath] = (string) preg_replace(
    '/\/\*\*.*?\*\/(\s*final class Laracord)/s',
    $facadeDocblock.'$1',
    $facadeSource,
    1
);

// Compare against disk.
$outOfDate = [];
foreach ($expected as $path => $contents) {
    if (! is_file($path) || file_get_contents($path) !== $contents) {
        $outOfDate[] = $path;
    }
}

if ($linting) {
    if ($outOfDate === []) {
        echo 'Generated resource clients are up to date.'.PHP_EOL;
        exit(0);
    }

    echo 'The following generated files are out of date:'.PHP_EOL;
    foreach ($outOfDate as $path) {
        echo ' - '.Str::after($path, $root.'/').PHP_EOL;
    }
    echo PHP_EOL.'Run: php -f bin/resources.php'.PHP_EOL;
    exit(1);
}

if (! is_dir($generatedDir)) {
    mkdir($generatedDir, 0755, true);
}

// Remove stale generated classes that no longer map to a resource.
$keep = array_map(fn (string $class) => $class.'.php', array_values($resourceAccessors));
foreach (glob($generatedDir.'/*.php') ?: [] as $existing) {
    if (! in_array(basename($existing), $keep, true)) {
        echo 'Removing stale '.basename($existing).PHP_EOL;
        unlink($existing);
    }
}

foreach ($expected as $path => $contents) {
    file_put_contents($path, $contents);
    echo 'Wrote '.Str::after($path, $root.'/').PHP_EOL;
}

echo 'Done.'.PHP_EOL;
exit(0);

/**
 * Render a reflection type as a fully-qualified docblock type string.
 */
function resolveReflectionType(?ReflectionType $type): string
{
    if ($type instanceof ReflectionUnionType) {
        return implode('|', array_map(resolveReflectionType(...), $type->getTypes()));
    }

    if ($type instanceof ReflectionIntersectionType) {
        return implode('&', array_map(resolveReflectionType(...), $type->getTypes()));
    }

    if (! $type instanceof ReflectionNamedType) {
        return '';
    }

    $name = $type->getName();
    $string = ($type->isBuiltin() || in_array($name, ['self', 'static', 'parent'], true) ? '' : '\\').$name;

    return $type->allowsNull() && $name !== 'null' && $name !== 'mixed' ? '?'.$string : $string;
}

/**
 * Render a parameter default value for a docblock.
 */
function resolveDefaultValue(ReflectionParameter $parameter): string
{
    if (! $parameter->isDefaultValueAvailable()) {
        return 'null';
    }

    $value = $parameter->getDefaultValue();

    return match (true) {
        $value === null => 'null',
        is_bool($value) => $value ? 'true' : 'false',
        is_string($value) => "'".$value."'",
        is_array($value) => '[]',
        default => (string) $value,
    };
}
